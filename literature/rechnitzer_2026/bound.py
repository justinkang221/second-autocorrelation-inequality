# cast the coefficients to mpf's and then normalise them
tmp = [mpf(x) for x in a_raw]
s = sum(tmp)
a = [x / s for x in tmp]

tmp = [mpf(x) for x in b_raw]
s = sum(tmp)
b = [x / s for x in tmp]  # we are using sum b = 1.


# We multiply the BesselJ functions by these amplitudes
j_amps = [factorial(p) * power(four_on_pi, p) for p in range(len(a) + 2)]


def big_J(p, k):
    return j_amps[p] * besselj(p, k * pi_on_two) / power(k, p)


def big_F(k):
    return power(sum([big_J(p, k) * v for p, v in enumerate(a)]), 4)


def big_G(k):
    return power(
        fabs(sum([big_J(p + 1, k) * v for p, v in enumerate(b)])), four_on_three
    )


# group the summands in 4s like this so that their asymptotics are better behaved.
# gives better series extrapolations
def the_F_summand(n):
    return big_F(4 * n - 3) + big_F(4 * n - 2) + big_F(4 * n - 1) + big_F(4 * n)


def the_G_summand(n):
    return big_G(4 * n - 3) + big_G(4 * n - 2) + big_G(4 * n - 1) + big_G(4 * n)


# use richardson extrapolations to estimate the infinite series
sum_F = nsum(the_F_summand, [1, inf], method="r", tol=epsilon, verbose=True)
sum_G = 2 * nsum(the_G_summand, [1, inf], method="r", tol=epsilon, verbose=True)

print("Sum of F(k)^4:\n\t", nstr(sum_F, 64))
print("Sum of abs(G(k))^(4/3):\n\t", nstr(sum_G, 64))

nu_u = half + sum_F
